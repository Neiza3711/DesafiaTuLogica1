public class NumerosDel10al1 {
    public static void main(String[] args) {
    	// Iniciamos desde el número 10
        int numero = 10; 

        // Utilizamos un bucle while para mostrar los números del 10 al 1
        while (numero >= 1) {
            System.out.println(numero);
            numero--; 
         // Los número se colocan de forma descendente 
        }
    }
}